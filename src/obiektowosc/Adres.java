package obiektowosc;

public class Adres {
	String ulica;
	int numerDomu;
	int numerMieszkania;
	int kodPocztowy;
	
	Adres(String ulica, int numerDomu, int numerMieszkania, int kodPocztowy){
		this.ulica = ulica;
		this.numerDomu = numerDomu;
		this.numerMieszkania = numerMieszkania;
		this.kodPocztowy = kodPocztowy;
	}

}
